                                   	<br>

<h5>El trabajo final de Laboratorio de Software fue pensado como una aplicación web de un sistema de guardia de una clínica o un hospital. <br>
<br>
Este cuenta con usuarios de sistema con roles definidos como: “Administrador”, “Personal Administrativo”, “Médico”, y “Jefe Médico”, cada uno con permisos distintos dentro de la aplicación acordes a su rol todos pueden realizar el Login correspondiente para el ingreso. <br>
<br>
El mismo tiene como objetivo principalmente el registro de pacientes que ingresan a la guardia, esto es realizado por Personal Administrativo. Una vez registrado, el paciente ingresa  a una lista de espera, para ser atendido por un Médico. <br>
<br>
En la pantalla denominada turnero, se podrá visualizar un tiempo estimado de atención. Los pacientes que ingresan con prioridad 1 o 2 no aparecerán en esta pantalla ya que consideramos de intención inmediata. <br>
<br>
El Médico podrá seleccionar el paciente de una lista de consultas y cargar todos los datos que requiera la atención del mismo. Toda esta información es almacenada en una base de datos, la cual será consultada cada vez que se necesite. Así mismo, se podrá tener acceso a la historia clínica de los pacientes atendidos a través de una búsqueda realizada por DNI. <br>
<br>
El Jefe Médico, tiene los mismos permisos que un Médico solo que este además, es el único que puede conformar la guardia del día, seleccionando así la fecha y el personal médico que la integra. <br>
<br>
Para finalizar el Administrador, tiene acceso a todas la funcionalidades del sistema, CRUD.
